#!/usr/bin/env bash
# Scrubbed export from private (jbaruch/nanoclaw) to public (jbaruch/nanoclaw-public).
# Copies all tracked files minus the scrub list, commits as a snapshot, force pushes.
#
# Usage: ./scripts/sync-to-public.sh [--dry-run]

set -euo pipefail

PRIVATE_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
PUBLIC_DIR="${PUBLIC_REPO_DIR:-$HOME/Projects/nanoclaw-public}"

if [ ! -d "$PUBLIC_DIR/.git" ]; then
  echo "ERROR: Public repo not found at $PUBLIC_DIR"
  echo "Set PUBLIC_REPO_DIR or clone jbaruch/nanoclaw-public to ~/Projects/nanoclaw-public"
  exit 1
fi

DRY_RUN=false
if [ "${1:-}" = "--dry-run" ]; then
  DRY_RUN=true
fi

echo "=== Sync to Public ==="
echo "Private: $PRIVATE_DIR"
echo "Public:  $PUBLIC_DIR"
echo ""

# --- Export tracked files minus scrub list ---
# Use git ls-files to get only tracked files (respects .gitignore)
cd "$PRIVATE_DIR"

# Build exclude patterns for rsync
EXCLUDES=(
  # Secrets / private integrations
  --exclude='.env'
  --exclude='scripts/trakt-auth.py'
  --exclude='scripts/audible-backup.sh'
  --exclude='src/hubitat-listener.ts'

  # Personal content
  --exclude='groups/global/SOUL.md'
  --exclude='groups/global/SOUL-untrusted.md'
  --exclude='groups/global/HEARTBEAT.md'
  --exclude='groups/global/CLAUDE.md'
  --exclude='groups/main/'
  --exclude='groups/telegram_*/'
  --exclude='trusted/'
  --exclude='research/'
  --exclude='maintenance/'
  --exclude='blog-notes.md'
  --exclude='docs/OPERATIONS.md'

  # Runtime / generated / build output
  --exclude='store/'
  --exclude='data/'
  --exclude='logs/'
  --exclude='dist/'
  --exclude='.claude-memory/'
  # Whole `.claude/` dir: mix of Claude Code settings (sandbox config,
  # local permissions) and runtime state (scheduled_tasks.lock with pid
  # + session id). None of it is NanoClaw source — users installing
  # NanoClaw create their own on first run. Pre-existing `.claude/`
  # content on public main (e.g. feature-skill SKILL.md files) is
  # preserved by the rsync-exclude semantics (neither added nor deleted).
  --exclude='.claude/'
  --exclude='container/agent-runner/dist/'
  --exclude='.DS_Store'
  --exclude='._*'

  # Tessl vendored tile content + generated RULES.md. These are
  # host-operational artifacts (the nanoclaw-host tile documents the
  # private/public repo-chain, promote pipeline, host conventions) and
  # don't belong in the public source tree. Both are gitignored on
  # private and in main via .gitignore, but rsync doesn't read
  # .gitignore — without these excludes the host-side tree leaks into
  # public on every sync.
  --exclude='.tessl/tiles/'
  --exclude='.tessl/RULES.md'

  # Keep .git in public untouched
  --exclude='.git/'
)

echo "Syncing files..."
rsync -a --delete \
  "${EXCLUDES[@]}" \
  "$PRIVATE_DIR/" "$PUBLIC_DIR/"

# Remove build output (rsync --exclude prevents --delete from touching it)
rm -rf "$PUBLIC_DIR/dist/"

# Retire stale host-tile content from the public repo's tracked files.
# Public used to sync `.tessl/tiles/jbaruch/nanoclaw-host/` and
# `.tessl/RULES.md` before the excludes above were added, so those
# paths may still be tracked in the public repo even though rsync no
# longer updates them. The file contents are host-operational
# (repo-chain, promote, etc.) and don't belong in the public fork.
# `git rm` stages their removal from the working tree/index for the
# next sync commit — `git rm` does NOT rewrite history, so older
# commits on public still contain them; only going-forward commits do
# not. The `[ -n "$(git ls-files ...)" ]` checks make this idempotent:
# first sync stages the removals, subsequent syncs see zero tracked
# paths and no-op.
(
  cd "$PUBLIC_DIR"
  if [ -n "$(git ls-files .tessl/tiles/jbaruch/nanoclaw-host/)" ]; then
    git rm -rf .tessl/tiles/jbaruch/nanoclaw-host/
    echo "  .tessl/: retired host-tile directory from public git"
  fi
  if [ -n "$(git ls-files .tessl/RULES.md)" ]; then
    git rm .tessl/RULES.md
    echo "  .tessl/: retired RULES.md from public git"
  fi
  # AGENTS.md is a tessl-managed bootstrap file (`@.tessl/RULES.md`
  # include). With RULES.md retired above, the include would dangle;
  # with AGENTS.md in .gitignore (added by this same sync), consumers
  # regenerate it via `tessl install` just like RULES.md. Retire the
  # stale tracked copy so public's main doesn't carry a broken include.
  if [ -n "$(git ls-files AGENTS.md)" ]; then
    git rm AGENTS.md
    echo "  AGENTS.md: retired from public git (regenerated by tessl install)"
  fi
)

# --- Apply in-file scrubs ---
echo "Scrubbing files..."

# Private handlers and MCP tools that must be scrubbed before the sync
# reaches public. These are TWO INDEPENDENT SURFACES:
#   - IPC handlers live in `src/ipc.ts` (the switch in the IPC dispatcher)
#   - MCP tools live in `container/agent-runner/src/ipc-mcp-stdio.ts`
#     (the `server.tool()` calls the containerized agent exposes)
# A given private integration may expose one, the other, or both — list
# its name in each list that applies. The two lists are NOT required to
# be identical (e.g. `smarthome_status` is an MCP tool with no matching
# IPC handler). Defense in depth: the allowlist verifier at the bottom
# of this script enumerates every handler/tool in the scrubbed tree and
# aborts the sync if anything isn't explicitly approved — so even if
# you forget to add a name here, the verifier catches it before the
# export lands on public.
PRIVATE_IPC_HANDLERS=(
  'sync_tripit'
  'fetch_trakt_history'
  'sessionize_get_event'
  'sessionize_open_cfps'
  'audible_backup'
  'dominos_pizza'
)
PRIVATE_MCP_TOOLS=(
  'sync_tripit'
  'fetch_trakt_history'
  'sessionize_get_event'
  'sessionize_open_cfps'
  'audible_backup'
  'dominos_pizza'
  'smarthome_status'
)

# 1. Remove private case blocks from ipc.ts
# Anchor on 6-space break (case-level) + blank line to avoid matching inner breaks
python3 -c "
import re
f = '$PUBLIC_DIR/src/ipc.ts'
code = open(f).read()
for name in $(printf "'%s', " "${PRIVATE_IPC_HANDLERS[@]}" | python3 -c "import sys; print('[' + sys.stdin.read().rstrip(', ') + ']')"):
    code = re.sub(r\"    case '\" + name + r\"':.*?\n      break;\n\n\", '', code, flags=re.DOTALL)
open(f, 'w').write(code)
print('  ipc.ts: removed private IPC handlers')
"

# 2. Remove private MCP tools from ipc-mcp-stdio.ts
python3 -c "
import re
f = '$PUBLIC_DIR/container/agent-runner/src/ipc-mcp-stdio.ts'
code = open(f).read()
# Anchor closing ); at start of line (0 indent) to avoid matching inner closings
for name in $(printf "'%s', " "${PRIVATE_MCP_TOOLS[@]}" | python3 -c "import sys; print('[' + sys.stdin.read().rstrip(', ') + ']')"):
    code = re.sub(r\"server\.tool\(\n  '\" + name + r\"',.*?\n\);\n\n\", '', code, flags=re.DOTALL)
open(f, 'w').write(code)
print('  ipc-mcp-stdio.ts: removed private MCP tools')
"

# 3. Remove Hubitat from config.ts
python3 -c "
import re
f = '$PUBLIC_DIR/src/config.ts'
code = open(f).read()
# Remove Hubitat config block
code = re.sub(r\"// --- Hubitat Smart Home ---.*?as 'low' \| 'medium' \| 'high';\n\", '', code, flags=re.DOTALL)
# Remove Hubitat entries from readEnvFile
code = re.sub(r\"  'HUBITAT_HUB_IP',\n\", '', code)
code = re.sub(r\"  'HUBITAT_APP_ID',\n\", '', code)
code = re.sub(r\"  'HUBITAT_EVENT_RETENTION_DAYS',\n\", '', code)
code = re.sub(r\"  'HUBITAT_ALERT_SENSITIVITY',\n\", '', code)
open(f, 'w').write(code)
print('  config.ts: removed Hubitat config')
"

# 4. Remove Hubitat from index.ts
python3 -c "
import re
f = '$PUBLIC_DIR/src/index.ts'
code = open(f).read()
code = re.sub(r\"import \{\n  startHubitatListener,\n  stopHubitatListener,\n\} from '\./hubitat-listener\.js';\n\", '', code)
code = re.sub(r\"  // Start Hubitat smart home listener.*?startHubitatListener\(\);\n\", '', code, flags=re.DOTALL)
code = re.sub(r\"    stopHubitatListener\(\);\n\", '', code)
open(f, 'w').write(code)
print('  index.ts: removed Hubitat listener')
"

# 5. Remove smart_home_events from db.ts
python3 -c "
import re
f = '$PUBLIC_DIR/src/db.ts'
code = open(f).read()
# Remove smart_home_events table creation
code = re.sub(r\"\n    CREATE TABLE IF NOT EXISTS smart_home_events.*?CREATE INDEX IF NOT EXISTS idx_she_device_time.*?\n\", '\n', code, flags=re.DOTALL)
# Remove smart home functions and interface
code = re.sub(r\"// --- Smart Home event accessors ---.*?// --- JSON migration ---\", '// --- JSON migration ---', code, flags=re.DOTALL)
open(f, 'w').write(code)
print('  db.ts: removed smart home schema and functions')
"

# 6. Remove reclaim-tripit from Dockerfile
if [ -f "$PUBLIC_DIR/Dockerfile.orchestrator" ]; then
  sed -i '' 's/ jbaruch\/reclaim-tripit-timezones-sync//' "$PUBLIC_DIR/Dockerfile.orchestrator"
  echo "  Dockerfile.orchestrator: removed reclaim-tripit package"
fi

# 7. Create generic SOUL-untrusted.md (excluded by rsync but Dockerfile.orchestrator needs it)
cat > "$PUBLIC_DIR/groups/global/SOUL-untrusted.md" << 'SOUL_EOF'
# Soul — Public Identity

